class Mdl_Site_Yuyue extends Mdl_Table
{   
  
    protected $_table = 'site_yuyue';
    protected $_pk = 'yuyue_id';
    protected $_cols = 'yuyue_id,site_id,mobile,contact,content,dateline,create_ip,is_read';

    protected $_orderby = array('yuyue_id'=>'desc');
    public function create($data, $checked=false)
    {
        if(!$checked && !$data = $this->_check_schema($data)){
            return false;
        }
        return $this->db->insert($this->_table, $data, true);
    }

    public function update($pk, $data, $checked=false)
    {
        $this->_checkpk();
        if(!$checked && !$data = $this->_check_schema($data,  $pk)){
            return false;
        }
        return $this->db->update($this->_table, $data, $this->field($this->_pk, $pk));
    }
}